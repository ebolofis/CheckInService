function BaseInfoModel(model) {
    var baseInfoModel = this;
    baseInfoModel.HotelId = model.HotelId;
    baseInfoModel.Mode = model.GroupModeEnum;
    baseInfoModel.Group = model.Group;
    baseInfoModel.Count = model.Count;
    baseInfoModel.HotelDescription = model.HotelDescription;
    baseInfoModel.Status = model.Status;
    baseInfoModel.String1 = model.String1;
};


